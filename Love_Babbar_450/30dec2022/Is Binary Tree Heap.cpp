// for a binary tree structure to be heap, all the levels should be completely from left tp right filled except last level 
class Solution {
  public:
    bool isHeap(struct Node* tree) {
        if( !tree or (!tree->left and !tree->right))// means ydi akeli node h to
            return true;
        
        if( !tree->left and tree->right )// ydi left exist nahi krta but right krta h means this tree is not heap
            return false;
        
        if (!tree->right && tree->left->left )
            return false;
            
        if (tree->right && tree->right->left && !tree->left->right)
            return false;

        if (tree->left->data > tree->data)// left wale ka data bda h instead of node
            return false;
            
        if (tree->right && tree->right->data > tree->data)//
            return false;
        
        return isHeap( tree->left ) and isHeap( tree->right );
        
    }
    
};

